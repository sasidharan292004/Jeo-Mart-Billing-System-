    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        String BID = bid.getText();
        String BNAME = bname.getText();
        String BMAIL_ID = bmail_id.getText();
        String BMOBILE = bmobile_no.getText();
        String BADDRESS = baddress.getText();
        String imgpath = "";
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setFileFilter(new javax.swing.filechooser.FileNameExtensionFilter("Image File", "JPG", "JPEG", "PNG"));
        int resultvalue = fileChooser.showOpenDialog(this);
        if (resultvalue == JFileChooser.APPROVE_OPTION) {
            File file = fileChooser.getSelectedFile();
            imgpath = file.getAbsolutePath();
            bphoto_path.setText(file.getName());
            bphoto.setIcon(new ImageIcon(new ImageIcon(imgpath).getImage().getScaledInstance(bphoto.getWidth(), bphoto.getHeight(), Image.SCALE_SMOOTH)));
        }

        if (!BID.isEmpty() && !BNAME.isEmpty() && !BMAIL_ID.isEmpty() && !BMOBILE.isEmpty() && !BADDRESS.isEmpty() && !imgpath.isEmpty()) {
            try (Connection conn = JDBC.con(); PreparedStatement pst = conn.prepareStatement("sp_jeomart_buyer_update ?,?,?,?,?,?")) {
                pst.setString(1, BID);
                pst.setString(2, BNAME);
                pst.setString(3, BMAIL_ID);
                pst.setString(4, BMOBILE);
                pst.setString(5, BADDRESS);
                pst.setString(6, imgpath);
                int result = pst.executeUpdate();
                if (result == 1) {
                    JOptionPane.showMessageDialog(this, "Datas Updated");
                } else {
                    JOptionPane.showMessageDialog(this, "Datas Can't Update");
                }

            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Enter All Datas");
        }

    }//GEN-LAST:event_updateActionPerformed

