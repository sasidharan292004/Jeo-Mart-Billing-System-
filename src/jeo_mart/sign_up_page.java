    private void sign_upActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sign_upActionPerformed
        String uid = user_id.getText();
        String Name = name.getText();
        String Password = password.getText();
        String Sec_Question = sec_question.getText();
        String Sec_Answer = sec_answer.getText();
        if (!uid.isEmpty() && !Name.isEmpty() && !Password.isEmpty() && !Sec_Question.isEmpty() && !Sec_Answer.isEmpty()) {
            if (!isExists(uid)) {
                try (Connection conn = JDBC.con(); PreparedStatement pst = conn.prepareStatement("sp_jeomart_admin_insert ?,?,?,?,?")) {
                    pst.setString(1, uid);
                    pst.setString(2, Name);
                    pst.setString(3, Password);
                    pst.setString(4, Sec_Question);
                    pst.setString(5, Sec_Answer);

                    int result = pst.executeUpdate();
                    if (result == 1) {
                        JOptionPane.showMessageDialog(this, " Successfully Sign-Up ");
                        this.setVisible(false);
                        new log_in_page().setVisible(true);
                    } else {
                        JOptionPane.showMessageDialog(this, " Can't Sign-Up ");
                    }

                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, e);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Same ID Allready Exists ");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Enter All Fields");
        }
    }//GEN-LAST:event_sign_upActionPerformed

