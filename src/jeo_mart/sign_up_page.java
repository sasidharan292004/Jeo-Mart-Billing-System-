    private boolean isExists(String ID) {
        try (Connection conn = JDBC.con(); PreparedStatement pst = conn.prepareStatement("sp_jeomart_admin_isExists ?")) {
            pst.setString(1, ID);
            ResultSet rs = pst.executeQuery();
            if (rs.next()) {
                return rs.getInt(1) > 0;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
        return false;
    }

